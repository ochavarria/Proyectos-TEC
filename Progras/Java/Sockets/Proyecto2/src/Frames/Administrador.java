    private void CrearGraficoCircular() {                                         
        Dimension d = PGrafico.getSize();
        DefaultPieDataset dataset = new DefaultPieDataset();

        // Establecemos unas categorÃ­as de ejemplo
        dataset.setValue("ColaRoja", 2);
        dataset.setValue("ColaAmarilla", 3);
        dataset.setValue("ColaVerde", Server.getLargo_ColaVerde());

        // Creamos la tabla
        grafico = ChartFactory.createPieChart(
                "Colas",
                dataset,
                true,
                true,
                false);
        JLabel j = new JLabel();
        j.setBounds(0, 0, d.width, d.height);

        _image = grafico.createBufferedImage(d.width, d.height);
        ImageIcon imagenFondo = new ImageIcon(_image);
        j.setIcon(imagenFondo);
        j.repaint();

        PGrafico.add(j);
        this.repaint();
    }                                        

